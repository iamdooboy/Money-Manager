import UIKit

extension UILabel {
    
    func setExpensePrice() {
        self.textColor = #colorLiteral(red: 0.8117647059, green: 0.2196078431, blue: 0.231372549, alpha: 1)
    }
    
    func setIncomePrice() {
        self.textColor = #colorLiteral(red: 0.1960784314, green: 0.5921568627, blue: 0.2078431373, alpha: 1)
    }
    
}

